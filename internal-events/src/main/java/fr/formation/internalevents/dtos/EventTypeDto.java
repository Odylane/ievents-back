package fr.formation.internalevents.dtos;

public class EventTypeDto {

	private Long id;

	private String name;

	public EventTypeDto() {

	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
